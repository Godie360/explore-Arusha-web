            <p>
            <h5>Ol Doinyo Lengai</h5>
            East Africa's only active volcano, Ol Doinyo Lengai, is situated south of Lake Natron. In the Maasai the
            name means the sacred Mountain of God. Mount Lengai is 878m high and it has explosive eruptions every twenty
            to forty years.

            The last bigger eruption was in 1966, with some smaller ones in 1983, 1933 and 2007. Ol Doinyo Lengai is one
            of the few volcanoes without Sodium carbonate and potassium and it is the only volcano in the world that
            erupts natrocarbonatite lava. This lava has almost no silicon and its temperatures are not as high as those
            of other lavas, approximately 500 – 600°C.
            </p>
            <p>

                The natrocarbonatite lava looks like fluid black oil and it turns white when in contact with objects
                full of moisture. This whitening happens almost immediately when raining.The mountain has two summit
                craters. The southern one is inactive and filled with volcanic ash. The active one is the northern
                summit crater, with a depth of over 200 meters. Mount Lengai can be climbed up to the peak and sometimes
                also all the way down to the crater, almost to the brim of the molten lava flows.

                Climbs are mostly organized after midnight to reach the summit in the sunrise.Although the rout is hard
                because of the heat ( 40°C) and hikers are advised to be extremely cautious, the views from Mount Lengai
                are astonishing. In the north Lake Natron is visible, in the west the Great Rift Valley, and the eastern
                part offers majestic views of Mount Kilimanjaro.
            </p>

