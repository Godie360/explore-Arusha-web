            Conservation of the area goes back to 1907 when it was a cattle ranch
            but in 1960 the area was set aside as Ngurdoto Crater National Park
            and after annexation of Mt. Meru the park name was changed to
            Arusha National Park with an area of 322 km2.
            The park can be accessed from Arusha town by road, which is about
            32 Kilometers away. It is about 40km from Kilimanjaro International
