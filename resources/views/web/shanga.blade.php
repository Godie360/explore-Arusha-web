                    <p>
                    <h5>Brief History SHANGA</h5>

                    Shanga, founded by a mother with two disabled sons seeking sustainable employment opportunities,
                    began with the production of headbands using beautiful fabrics. Over time, the enterprise expanded
                    significantly, now encompassing blanket making, jewelry crafting, glass blowing, Tinga Tinga
                    painting, and more. Visitors to Shanga can tour these diverse departments, where they have the
                    opportunity to learn Swahili sign language and interact directly with artisans. The shop offers a
                    variety of handmade items, such as an elephant pillow made from recycled t-shirts donated to the
                    workshop.

                    This social enterprise reflects Tanzania's progress in integrating disabled individuals into the
                    workforce and society. Previously marginalized and unemployed, these individuals now contribute
                    actively to Shanga's vibrant community. Supporting Shanga means supporting its mission of
                    empowerment through skill development and meaningful employment. For anyone in Arusha, Tanzania,
                    visiting Shanga offers not only the chance to purchase unique handmade goods but also to learn about
                    its inspiring history and impact firsthand.
