            <h5>Brief History Of Oldvai Gorge</h5>
            The Olduvai Gorge or Oldupai Gorge is a paleoanthropological site in the eastern Serengeti Plain, near
            northern Tanzania, in East Africa. The gorge is nicknamed the “Cradle of Mankind” because it is believed to
            be the site of found remains of the first human beings to walk on Earth. Recent discoveries in Ethiopia and
            South Africa, however, push the age of the first humans back further in time
            The Olduvai gorge was created by erosion that spanned over 2 million years. It is a steep ravine at 30
            miles (48 km) wide, 295 feet (90 meters) deep and over 30 miles (48 km) long. A side gorge joins the main
            gorge about 12 miles (19 km) downstream of Lake Masek and Lake Ndutu. The word olduvai comes from the Maasai
            language of the Maasai people and means “the place of the wild sisal.” Sisal is an abundant plant throughout
            the gorge.
            </p>

            <p>
            
            German physician and archaeologist Wilhelm Kattwinkel was traveling in German East Africa in 1911 and
            visited the area in hopes of finding artifacts. Kattwinkel found several fossil bones of an extinct
            three-toed horse, and his discovery inspired others to visit the area. Geologist Hans Reck led a team to the
            gorge in 1913, and hominin remains were discovered. World War I halted expeditions for some years; in 1931
            Louis Leakey led a team to the gorge. Leakey found several hand axes, and returned to the gorge in 1935 with
            his wife, Mary, and geologist Percy Edward Kent.

            The Olduvai Gorge Museum was first dedicated in the late 1970s by Mary Leakey, and was replaced by a new
            building in 2018, the same year the Olduvai Gorge Monument was erected. Created by artist Fest Kijo, the
            monument features two large concrete skulls, depicting the first two species found at the gorge. Exhibits at
            the museum have featured artifacts and research from the surrounding area since its inception. Daily
            lectures are offered by the Antiquities guides, and safaris are available to view migrations of wildebeest,
            zebra, lion, elephant, and cheetah that annually pass through the gorge. Other animals seen in the gorge are
            giraffe, agama lizard, leopard tortoise, dik-dik antelope and Grant’s gazelle.

